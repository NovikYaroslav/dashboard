export function alarmHours() {
  let hours = [];
  for (let i = 0; i < 24; i++) {
    hours.push(i);
  }
  return hours;
}

export function alarmMinutes() {
  let minutes = [];
  for (let i = 0; i < 60; i++) {
    minutes.push(i);
  }
  return minutes;
}

export function calculatorNumbers() {
  let numbers = [];
  for (let i = 0; i < 10; i++) {
    numbers.push(i);
  }
  return numbers;
}
